    //metodo que se encarga de verificar que tipo de planta selecciono para crear el jugador 
    public int evaluarTipoDePlanta() {
        String tipoDePlanta = (String) cmbTipoDePlanta.getSelectedItem();
        try {
            if (tipoDePlanta.equals("Planta Grano")) {
                return 1;
            } else if (tipoDePlanta.equals("Planta Fruta")) {
                return 2;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Selecciona el tipo de planta", "Error", JOptionPane.ERROR_MESSAGE);
        }
        return -1;
    }

