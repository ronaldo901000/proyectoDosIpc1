    @Override
    public void run() {
        while (granja.getGranjero().isEstaVivo() && !granja.isActualizarPanelGranja() && !granja.getGranjero().isSalioDeLaPartida()) {
            actualizarHpGranjero();
            if (granja.getGranjero().getPuntosDeVida() <= 0) {
                granja.getGranjero().setEstaVivo(false);
            }
            actualizarHpGranjero();
        }
        if (!granja.getGranjero().isEstaVivo()) {
            PartidaAcabadaDialog dialog = new PartidaAcabadaDialog();
            dialog.setVisible(true);
        }
    }

    public void actualizarHpGranjero() {
        lblVida.setText("HP:" + granja.getGranjero().getPuntosDeVida() + "/" + granja.getGranjero().getLIMITE_PUNTOS_DE_VIDA());
        if (granja.getGranjero().getPuntosDeVida() <= 20 && !yaSeAviso) {
            JOptionPane.showMessageDialog(this, "Debes comer, te queda poca vida", "Comer", JOptionPane.ERROR_MESSAGE);
            yaSeAviso = true;
        }
    }

