    public void cargarTablaYCmbBox() {
        DefaultTableModel modelo = (DefaultTableModel) tablaParcelas.getModel();
        for (int i = 0; i < granja.getParcelas().getTamaño(); i++) {
            modelo.addRow(new Object[]{(i + 1), granja.getParcelas().obtenerContenido(i).getNombre(), granja.getParcelas().obtenerContenido(i).getSuelos().getTamaño()});
            cmbIndiceParcela.addItem("" + (i + 1));
        }
    }
    
