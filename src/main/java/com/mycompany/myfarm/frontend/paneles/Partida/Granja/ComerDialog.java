    public void actualizarTabla(){
        DefaultTableModel modelo =(DefaultTableModel) tablaAlimentos.getModel();
        int filas=modelo.getRowCount();
        for (int i = filas-1; i>=0; i--) {
            modelo.removeRow(i);
        }
        cmbNumeroAlimento.removeAllItems();
        for (int i = 0; i < bodega.getProductosGuardados().getTamaño(); i++) {
            if (bodega.getProductosGuardados().obtenerContenido(i) instanceof Alimento) {
                Alimento alimento =(Alimento)bodega.getProductosGuardados().obtenerContenido(i);
                modelo.addRow(new Object[]{(i + 1),alimento.getNombre()});
                cmbNumeroAlimento.addItem(""+(i+1));
            }
        }
        
    }

