    private String obtenerNombre() {
        String nombre = txtNombre.getText();
        if (!nombre.equals("")) {
            return nombre;
        }
        JOptionPane.showMessageDialog(this, "Debes ingresar un nombre para el alimento", "Falta nombre", JOptionPane.ERROR_MESSAGE);
        return null;
    }
    
    private int evaluarTipoDeAlimento(){
        int tipoDeAlimento=cmbTipoDeAlimento.getSelectedIndex();
        if(tipoDeAlimento==-1){
            JOptionPane.showMessageDialog(this,"Debes seleccionar el tipo de Alimento","Falta tipo de alimento",JOptionPane.ERROR_MESSAGE);
        }
        return tipoDeAlimento;
    }

    
    private void inicializarComboPrecioDeVenta(){
        for(int i=1; i<=200; i++){
            cmbPrecioDeVenta.addItem(""+i);
        }
    }

