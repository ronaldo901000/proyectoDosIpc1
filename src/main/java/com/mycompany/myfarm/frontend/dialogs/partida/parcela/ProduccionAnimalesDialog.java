    private void btnPorParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPorParcelaActionPerformed
        if (parcela.hayAnimalesVivos()) {
            String listaProductos = parcela.hacerListaProductos();
            parcela.procesarParcela(bodega);
            AccionExitosaDialog dialog = new AccionExitosaDialog("Se procesaron: " + listaProductos);
            dialog.setVisible(true);
            this.dispose();
        }
        else{
            JOptionPane.showMessageDialog(this, "No hay animales vivos por procesar", "Sin animales vivos", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnPorParcelaActionPerformed

    private void btnParcelaProduccionAlimentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnParcelaProduccionAlimentosActionPerformed
        if (parcela.elAnimalGeneraAlimentos()) {
            String listaProductos = parcela.hacerListaAlimentos();
            parcela.procesarParcelaProduccionAlimento(bodega);
            AccionExitosaDialog dialog = new AccionExitosaDialog("Se procesaron: " + listaProductos);
            dialog.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Los animales de esta parcela no generan Alimentos", "Sin Alimentos", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnParcelaProduccionAlimentosActionPerformed

