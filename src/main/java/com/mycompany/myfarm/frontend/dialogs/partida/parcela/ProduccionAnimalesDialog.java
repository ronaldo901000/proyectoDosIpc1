    private void ParcelaProduccionConDestaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ParcelaProduccionConDestaceActionPerformed
        if (parcela.elAnimalGeneraProductosConDestace()) {
            String listaProductos = parcela.hacerListaProductosConDestace();
            parcela.procesarParcelaProduccionConDestace(bodega);
            parcela.actualizarIconoParcela(5);
            AccionExitosaDialog dialog = new AccionExitosaDialog("Se procesaron: " + listaProductos);
            dialog.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Los animales de esta parcela no generan alimentos al ser destazados", "Sin productos", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ParcelaProduccionConDestaceActionPerformed

