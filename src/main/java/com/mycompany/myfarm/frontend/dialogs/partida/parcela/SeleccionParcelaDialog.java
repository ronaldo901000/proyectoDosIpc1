    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        int indice = cmbIndiceParcela.getSelectedIndex();
        Parcela parcela = granja.getParcelas().obtenerContenido(indice);
        boolean hayEspacio;
        if (parcela.getAnimales().getTama単o()>0) {
            int espacioDeUnAnimal = (int) granja.getParcelas().obtenerContenido(indice).getAnimales().obtenerContenido(0).getEspacioEnParcela();
            int cantidadAnimales = parcela.getAnimales().getTama単o();
            int capacidadParcela = parcela.getCapacidad();
            int espacioDisponible = capacidadParcela - (espacioDeUnAnimal * cantidadAnimales);
            if(espacioDisponible>=espacioDeUnAnimal){
                hayEspacio=true;
            }
            else{
                hayEspacio=false;
            }
        }
        else{
            hayEspacio=true;
        }
        if (hayEspacio) {
            this.dispose();
            MercadoAnimalesDialog dialog = new MercadoAnimalesDialog(granja.getGranjero(), parcela, mercado);
            dialog.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "Parcela con capacidad llena","Parcela llena", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnSiguienteActionPerformed

