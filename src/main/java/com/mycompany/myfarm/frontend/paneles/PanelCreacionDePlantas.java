    private void btnCrearPlantaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPlantaActionPerformed
        String nombreDeLaPlanta = obtenerTexto(txtNombre);
        int cantidadDeSemillas = (Integer) spnCantidadSemillas.getValue();
        int precioCompraSemillaMercado = (Integer) spnPrecioCompraSemilla.getValue();
        int tipoPlanta = evaluarTipoDePlanta();
        
        if (nombreDeLaPlanta!=null  && tipoPlanta != -1) {
            Planta planta = creador.crearUnaPlanta(nombreDeLaPlanta, cantidadDeSemillas, tipoPlanta);
            Semilla semilla = new Semilla(planta, nombreDeLaPlanta, precioCompraSemillaMercado);
            MotorJuego.getAlmacenador().getMercadoGeneral().getSemillasDisponibles().agregarElemento(semilla);
            SeleccionProductosPlantaDialog dialgo= new SeleccionProductosPlantaDialog(tipoPlanta);
            dialgo.setVisible(true);
            AccionExitosaDialog dialog = new AccionExitosaDialog("Planta creada Correctamente");
            dialog.setVisible(true);
            limpiarCampos();
        }
    }//GEN-LAST:event_btnCrearPlantaActionPerformed
    
    public String obtenerTexto(JTextField txtTexto){
        String nombreDeLaPlanta = txtTexto.getText();
        if(!nombreDeLaPlanta.equals("")){
            return nombreDeLaPlanta;
        }
        JOptionPane.showMessageDialog(this,"Debes ingresar un nombre para la planta","Falta dato",JOptionPane.ERROR_MESSAGE);
        return null;
    }
    
    public void limpiarCampos(){
        txtNombre.setText("");
        spnCantidadSemillas.setValue(1);
        spnPrecioCompraSemilla.setValue(0);
        cmbTipoDePlanta.setSelectedItem(null);
    }
    
