    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        int seleccionado = cmbFertilizantes.getSelectedIndex();
        Fertilizante fertilizante = fertilizantes[seleccionado];
        int precio = fertilizante.getPrecio();
        if (granjero.getOroDisponible() >= precio) {
            if (!sueloSeleccionado.isYaTieneFertilizante()) {
                granjero.pagar(precio);
                sueloSeleccionado.setYaTieneFertilizante(true);
                sueloSeleccionado.setCalidadDelSuelo(fertilizante.getCalidad());
                JOptionPane.showMessageDialog(this, "Compra exitosa", "Compra", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }

        } else {
            JOptionPane.showMessageDialog(this, " No cuentas con dinero para comprar", "Compra", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_btnComprarActionPerformed

