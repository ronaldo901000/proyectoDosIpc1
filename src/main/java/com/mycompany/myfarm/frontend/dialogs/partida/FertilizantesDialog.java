    private void cargarLosFertilizantes(){
        DefaultTableModel modelo = (DefaultTableModel) tablaFertilizantes.getModel();
        for(int i=0; i<fertilizantes.length; i++){
            cmbFertilizantes.addItem(""+fertilizantes[i].getNombre());
            modelo.addRow(new Object[]{fertilizantes[i].getNombre(),fertilizantes[i].getCalidad(),fertilizantes[i].getPrecio()});
        }
    }
    
