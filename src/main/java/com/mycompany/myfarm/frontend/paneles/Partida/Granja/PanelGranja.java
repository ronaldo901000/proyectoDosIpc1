    public void colocarPanelesEnTerreno(Granja granja) {
        panelTerreno.removeAll();
        for (int i = 0; i < granja.getFilas(); i++) {
            for (int j = 0; j < granja.getColumnas(); j++) {
                if (granja.getSuelos()[i][j] != null) {
                    panelTerreno.add(granja.getSuelos()[i][j].getPanel());
                    revalidate();
                    repaint();
                } else {
                    int[] posicion = new int[]{i, j};
                    panelTerreno.add(new PanelSueloEnVenta(posicion, granja));
                    revalidate();
                    repaint();
                }
            }
        }
    }

