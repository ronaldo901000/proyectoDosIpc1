    private void btnComerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComerActionPerformed
        if (bodega.getProductosGuardados().getTamaño() > 0) {
            try {
                int indiceComida = cmbNumeroAlimento.getSelectedIndex();
                    bodega.getProductosGuardados().eliminar(indiceComida);
                    granjero.agregarAlimentoConsumido();
                    int vidaTotal = granjero.getPuntosDeVida() + ENERGIA_POR_ALIMENTO;
                    if (vidaTotal > granjero.getLIMITE_PUNTOS_DE_VIDA()) {
                        granjero.setPuntosDeVida(granjero.getLIMITE_PUNTOS_DE_VIDA());
                    } else {
                        granjero.setPuntosDeVida(vidaTotal);
                    }
                    actualizarTabla();
                    //granjero.getGranja().getPanelGranja().getLblVida().setText("HP: " + granjero.getPuntosDeVida() + "/" + granjero.getLIMITE_PUNTOS_DE_VIDA());
                    AccionExitosaDialog accion = new AccionExitosaDialog("Has comido");
                    accion.setVisible(true);


            } catch (ListaEnlazadaException ex) {
                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ya no hay alimentos en bodega", "Sin alimentos", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnComerActionPerformed

