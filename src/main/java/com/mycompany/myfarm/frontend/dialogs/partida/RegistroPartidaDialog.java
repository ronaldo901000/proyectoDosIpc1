    private void btnIniciarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarPartidaActionPerformed
        String nombre = evaluarDato(txtNombre);
        String nick = evaluarDato(txtNick);
        String nombrePartida = evaluarDato(txtNombrePartida);
        if (nombre != null && nick != null && nombrePartida != null) {
            //Se crea el jugador y luego se le asigna a la partida
            if (!MotorJuego.getAlmacenador().nombreDePartidaExistente(nombrePartida)) {
                Granjero granjero = new Granjero(nombre, nick);
                Granja granja = new Granja(granjero);
                granjero.setGranja(granja);
                Partida partida = new Partida(nombrePartida, granja, granjero);
                granja.setPartida(partida);
                partida.iniciarPartida();
                this.dispose();
            }
            else{
                txtNombrePartida.setText("");
                JOptionPane.showMessageDialog(this, "Nombre de partida no disponible, utiliza otra", "Nombre existente", JOptionPane.ERROR_MESSAGE);
            }
        }
        if (nombre == null || nick == null || nombrePartida == null) {
            JOptionPane.showMessageDialog(this, "Te falto ingresar un dato verifica", "Falta dato", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnIniciarPartidaActionPerformed

