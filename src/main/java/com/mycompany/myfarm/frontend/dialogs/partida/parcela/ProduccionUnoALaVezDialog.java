    private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
        int indiceAnimal = cmbAnimalSeleccionado.getSelectedIndex();
        if (parcela.getAnimales().getTama単o() > 0) {
            Animal animal = parcela.getAnimales().obtenerContenido(indiceAnimal);
            int opcionDeProcesamiento = cmbProceasamiento.getSelectedIndex();
            if (opcionDeProcesamiento == 0) {
                if (animal.getProductosAcumulados().getTama単o() > 0) {
                    parcela.procesarUnAnimal(bodega, animal, opcionDeProcesamiento, indiceAnimal);
                    
                    actualizarTabla();
                    JOptionPane.showMessageDialog(this, "Procesamiento realizado con exito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "No hay productos por procesar", "Sin Productos", JOptionPane.ERROR_MESSAGE);
                }

            } else if (opcionDeProcesamiento == 1) {
                if (animal.isEsDestasable()) {
                    parcela.procesarUnAnimal(bodega, animal, opcionDeProcesamiento, indiceAnimal);
                    actualizarTabla();
                    JOptionPane.showMessageDialog(this, "Procesamiento realizado con exito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Procesamiento realizado con exito", "Correcto", JOptionPane.INFORMATION_MESSAGE);
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "No hay animales en la parcela", "Sin animales", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnProcesarActionPerformed
    private void actualizarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) tablaAnimales.getModel();
        int totalFilas = modelo.getRowCount();
        for (int i = totalFilas - 1; i >= 0; i--) {
            modelo.removeRow(i);

        }
        cmbAnimalSeleccionado.removeAllItems();
        for (int i = 0; i < parcela.getAnimales().getTama単o(); i++) {
            Animal animal = parcela.getAnimales().obtenerContenido(i);
            if (animal.isEstaVivo()) {
                modelo.addRow(new Object[]{(i + 1), animal.getNombre(), animal.getProductosAcumulados().getTama単o()});
                cmbAnimalSeleccionado.addItem("" + (i + 1));
            }

        }
    }

