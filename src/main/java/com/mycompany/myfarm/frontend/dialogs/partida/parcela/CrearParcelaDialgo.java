    private void cargarSuelosAPanel(Suelo[] []suelos){
        for (int i = 0; i < granja.getFilas(); i++) {
            for(int j=0;j<granja.getColumnas(); j++){
                if(suelos[i][j] instanceof SueloGrama){
                    SueloGrama suelo = (SueloGrama) suelos[i][j];
                    panelSuelos2.add(suelo.getPanelDisponibilidad());
                    suelo.getPanelDisponibilidad().inicilizarFondoYLabelInfo();
                    revalidate();
                    repaint();
                }
                else{
                    panelSuelos2.add(new PanelSinAccion());
                }
                revalidate();
                repaint();
            }
        }
    }
