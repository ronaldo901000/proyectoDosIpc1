    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        int indice = cmbIndiceParcela.getSelectedIndex();
        int accion = cmbAcciones.getSelectedIndex();
        Parcela parcela = granja.getParcelas().obtenerContenido(indice);

        switch (accion) {
            case 0: 
                if (!parcela.getAnimales().esVacia() && parcela.hayAnimalesVivos()) {
                    AlimentarAnimalesDialog dialog = new AlimentarAnimalesDialog(parcela, granja);
                    dialog.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No hay animales vivos en esta parcela", "Sin animales", JOptionPane.ERROR_MESSAGE);
                }
                break;

            case 1: 
                if (!parcela.getAnimales().esVacia() && parcela.hayAnimalesVivos()) {
                    ProduccionAnimalesDialog dialog = new ProduccionAnimalesDialog(parcela, granja.getBodega());
                    dialog.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No hay animales vivos en esta parcela", "Sin animales", JOptionPane.ERROR_MESSAGE);
                }
                break;

            case 2: 
                if (!parcela.getAnimales().esVacia() && parcela.contarAnimalesMuertos() > 0) {
                    LimpiezaParcelaDialog dialog = new LimpiezaParcelaDialog(parcela, granja.getGranjero());
                    dialog.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No hay animales muertos en esta parcela", "Sin animales muertos", JOptionPane.ERROR_MESSAGE);
                }
                break;

            case 3: 
                if (parcela.getAnimales().esVacia()) {
                    PagarEliminacionParcelaDialog dialog = new PagarEliminacionParcelaDialog(granja, indice);
                    dialog.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No se puede eliminar una parcela con animales dentro", "Parcela ocupada", JOptionPane.ERROR_MESSAGE);
                }
                break;

            default:
                
                break;
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

