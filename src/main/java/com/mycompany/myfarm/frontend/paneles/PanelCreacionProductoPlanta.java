    private void btnCrearAlimentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearAlimentoActionPerformed
        String nombre =obtenerNombre();
        int precioDeVenta =cmbPrecioDeVenta.getSelectedIndex()+1;
        int tipoDeAlimento = evaluarTipoDeAlimento();
        if(nombre!=null && tipoDeAlimento!=-1){
            boolean nombreDisponible=creador.elNombreEstaDisponible(nombre);
            if(nombreDisponible){
                Producto nuevoProducto = creador.crearUnNuevoProductoPlanta(nombre, precioDeVenta, tipoDeAlimento);
                MotorJuego.getAlmacenador().getProductosDisponibles().agregarElemento(nuevoProducto);
                limpiarCampos();
                AccionExitosaDialog dialog = new AccionExitosaDialog("Alimento creado correctamente");
                dialog.setVisible(true);
            }
        }
       
    }//GEN-LAST:event_btnCrearAlimentoActionPerformed

    private void limpiarCampos(){
        txtNombre.setText("");
    }
